// Promise 

//An object that is used as a placeholder for the future result of an asynchronous operation

// A container for an asynchronously delivered value

// A container for a future value.

// Example

// Promise that I will receive money if I guess correct outcome

// I buy lottery ticker(promise)
// Lottery draw happens asynchronously
// if correct outcome, I receive money, because it was promised.

// advantage of promise

// We no longer need to rely on events and callbacks passed ito asynchronous functions to handle asynchronous results;

// Instead of nesting callbacks, we can chain promises for a sequence of asynchronous operations: escaping callback hell.