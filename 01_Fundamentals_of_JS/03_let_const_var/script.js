// The above Keywords are used to declare a variable
// if you don't specify one of the keyword then your variable is not declared.

// difference between var and let 
// var is global scoped. 
// let is block scoped.

// Note = if you declare a varaible with const keyword remember to assign value to it if you don't assign value an error will occured.

let age = 19;
age = 20;

